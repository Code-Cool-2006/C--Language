#include <stdio.h>

int main() {
    int a, b, sum;

    // Prompt the user to enter two integers
    printf("Enter two integers: \n");
    scanf("%d %d", &a, &b );

    // Calculate the sum of the two integers
    sum = a + b;

    // Display the result
    printf(" %d " , sum);

    return 0;
}
